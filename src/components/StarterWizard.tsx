type WizardProps = {
  language: Language;
  showToast: (text: string, tone?: "success" | "danger") => void;
};

export function StarterWizard({ language, showToast }: WizardProps) {
  const [form, setForm] = usePersistentState<WizardFormState>(
    STORAGE_KEY,
    DEFAULT_STATE,
  );
  const [expandedOutput, setExpandedOutput] = useState<OutputKey | null>(null);

  const t = messages[language];

  const update = useCallback(
    <K extends keyof WizardFormState>(key: K, value: WizardFormState[K]) => {
      setForm((prev) => ({ ...prev, [key]: value }));
    },
    [setForm],
  );

  const updateSlug = useCallback(
    (value: string) => {
      setForm((prev: WizardFormState) => ({
        ...prev,
        projectName: value,
        repoSlug: toKebabCase(value),
      }));
    },
    [setForm],
  );

  const updateSlugDirect = useCallback(
    (value: string) => {
      setForm((prev: WizardFormState) => ({ ...prev, repoSlug: value }));
    },
    [setForm],
  );

  const togglePrivacy = useCallback(
    (model: PrivacyModel) => {
      setForm((prev: WizardFormState) => {
        const has = prev.privacyModel.includes(model);
        return {
          ...prev,
          privacyModel: has
            ? prev.privacyModel.filter((m: PrivacyModel) => m !== model)
            : [...prev.privacyModel, model],
        };
      });
    },
    [setForm],
  );

  const resetSample = useCallback(() => {
    setForm(SAMPLE_STATE);
    showToast(language === "zh" ? "已加载示例数据" : "Sample data loaded");
  }, [setForm, showToast, language]);

  const clearForm = useCallback(() => {
    setForm(DEFAULT_STATE);
    showToast(language === "zh" ? "表单已清空" : "Form cleared");
  }, [setForm, showToast, language]);

  const outputs = useMemo(() => {
    const generators: Record<OutputKey, () => string> = {
      spec: () => generateProjectSpec(form, language),
      prompt: () => generateOpenCodePrompt(form, language),
      command: () => generateCreateCommand(form),
      readme: () => generateReadmeDraft(form),
      checklist: () => generateChecklist(form),
      json: () => generateSpecJson(form),
      "repo-setup": () => generateRepoSetup(form),
      "release-draft": () => generateReleaseDraft(form),
      roadmap: () => generateRoadmapIssues(form),
      screenshots: () => generateScreenshotReminder(form),
      security: () => generateSecuritySummary(form),
      contributing: () => generateContributingSummary(form),
    };
    return Object.fromEntries(
      OUTPUTS.map((def) => [def.key, generators[def.key]()]),
    ) as Record<OutputKey, string>;
  }, [form, language]);

  const slugValid = !form.repoSlug || isKebabCase(form.repoSlug);
  const hasContent = form.projectName || form.description || form.coreFeatures;

  return (
    <section className="section wizard" data-testid="starter-wizard" id="wizard">
      <div className="section__header">
        <p className="eyebrow">
          <Wand2 size={14} aria-hidden="true" />{" "}
          {language === "zh" ? "项目启动向导" : "Starter Wizard"}
        </p>
        <h2>{t.wizard.title}</h2>
        <p>{t.wizard.intro}</p>
      </div>

      <div className="wizard__layout">
        {/* Form */}
        <div className="wizard__form" data-testid="wizard-form">
          <div className="wizard__form-actions">
            <Button
              icon={<Sparkles size={16} />}
              onClick={resetSample}
              size="sm"
              variant="ghost"
            >
              {t.wizard.resetSample}
            </Button>
            <Button
              icon={<Trash2 size={16} />}
              onClick={clearForm}
              size="sm"
              variant="ghost"
            >
              {t.wizard.clearForm}
            </Button>
          </div>

          {/* Project Identity */}
          <fieldset className="wizard__fieldset">
            <legend>{t.wizard.sectionIdentity}</legend>
            <label className="wizard__label">
              <span>{t.wizard.projectName}</span>
              <input
                className="wizard__input"
                data-testid="wizard-project-name"
                onChange={(e) => updateSlug(e.target.value)}
                placeholder={language === "zh" ? "如：JSON 格式化工具" : "e.g., JSON Formatter"}
                type="text"
                value={form.projectName}
              />
            </label>
            <label className="wizard__label">
              <span>{t.wizard.repoSlug}</span>
              <input
                className={`wizard__input ${!slugValid ? "wizard__input--error" : ""}`}
                data-testid="wizard-repo-slug"
                onChange={(e) => updateSlugDirect(e.target.value)}
                placeholder="json-formatter"
                type="text"
                value={form.repoSlug}
              />
              {!slugValid && (
                <span className="wizard__error">{t.wizard.slugError}</span>
              )}
            </label>
            <label className="wizard__label">
              <span>{t.wizard.level}</span>
              <div className="wizard__level-btns" data-testid="wizard-level">
                {(["C", "B", "A"] as ProjectLevel[]).map((lv) => (
                  <button
                    className={`wizard__level-btn ${form.level === lv ? "is-active" : ""}`}
                    key={lv}
                    onClick={() => update("level", lv)}
                    type="button"
                  >
                    {lv}
                  </button>
                ))}
              </div>
            </label>
            <label className="wizard__label">
              <span>{t.wizard.description}</span>
              <textarea
                className="wizard__textarea"
                data-testid="wizard-description"
                onChange={(e) => update("description", e.target.value)}
                placeholder={
                  language === "zh"
                    ? "一句话说明这个工具做什么"
                    : "One sentence about what this tool does"
                }
                rows={2}
                value={form.description}
              />
            </label>
          </fieldset>

          {/* Problem & User */}
          <fieldset className="wizard__fieldset">
            <legend>{t.wizard.sectionProblem}</legend>
            <label className="wizard__label">
              <span>{t.wizard.targetUser}</span>
              <input
                className="wizard__input"
                onChange={(e) => update("targetUser", e.target.value)}
                placeholder={language === "zh" ? "谁会用这个工具？" : "Who will use this?"}
                type="text"
                value={form.targetUser}
              />
            </label>
            <label className="wizard__label">
              <span>{t.wizard.corePain}</span>
              <textarea
                className="wizard__textarea"
                onChange={(e) => update("corePain", e.target.value)}
                placeholder={
                  language === "zh"
                    ? "用户现在遇到什么问题？"
                    : "What problem does the user face today?"
                }
                rows={2}
                value={form.corePain}
              />
            </label>
          </fieldset>

          {/* Features */}
          <fieldset className="wizard__fieldset">
            <legend>{t.wizard.sectionFeatures}</legend>
            <label className="wizard__label">
              <span>{t.wizard.coreFeatures}</span>
              <textarea
                className="wizard__textarea wizard__textarea--tall"
                data-testid="wizard-features"
                onChange={(e) => update("coreFeatures", e.target.value)}
                placeholder={
                  language === "zh"
                    ? "每行一个核心功能"
                    : "One core feature per line"
                }
                rows={5}
                value={form.coreFeatures}
              />
            </label>
            <label className="wizard__label">
              <span>{t.wizard.notDoing}</span>
              <textarea
                className="wizard__textarea wizard__textarea--tall"
                data-testid="wizard-not-doing"
                onChange={(e) => update("notDoing", e.target.value)}
                placeholder={
                  language === "zh"
                    ? "每行一个明确不做的事"
                    : "One non-goal per line"
                }
                rows={4}
                value={form.notDoing}
              />
            </label>
          </fieldset>

          {/* Technical Decisions */}
          <fieldset className="wizard__fieldset">
            <legend>{t.wizard.sectionTechnical}</legend>

            <label className="wizard__label">
              <span>{t.wizard.privacyModel}</span>
              <div className="wizard__check-group">
                {(
                  [
                    { key: "local-first", label: "Local First" },
                    { key: "no-upload", label: "No Upload" },
                    { key: "no-backend", label: "No Backend" },
                  ] as const
                ).map((opt) => (
                  <label className="wizard__check" key={opt.key}>
                    <input
                      checked={form.privacyModel.includes(opt.key)}
                      onChange={() => togglePrivacy(opt.key)}
                      type="checkbox"
                    />
                    <span>{opt.label}</span>
                  </label>
                ))}
              </div>
            </label>

            <div className="wizard__row">
              <label className="wizard__label wizard__label--half">
                <span>{t.wizard.deployTarget}</span>
                <select
                  className="wizard__select"
                  onChange={(e) => update("deployTarget", e.target.value as DeployTarget)}
                  value={form.deployTarget}
                >
                  <option value="github-pages">GitHub Pages</option>
                  <option value="netlify">Netlify</option>
                  <option value="vercel">Vercel</option>
                  <option value="other">Other</option>
                </select>
              </label>
              <label className="wizard__label wizard__label--half">
                <span>{t.wizard.textLang}</span>
                <select
                  className="wizard__select"
                  onChange={(e) => update("textLang", e.target.value as TextLang)}
                  value={form.textLang}
                >
                  <option value="zh">{language === "zh" ? "中文" : "Chinese"}</option>
                  <option value="en">English</option>
                  <option value="bilingual">{language === "zh" ? "中英双语" : "Bilingual"}</option>
                </select>
              </label>
            </div>

            <div className="wizard__row">
              <label className="wizard__label wizard__label--half">
                <span>{t.wizard.lsNeed}</span>
                <select
                  className="wizard__select"
                  onChange={(e) => update("lsNeed", e.target.value as LsNeed)}
                  value={form.lsNeed}
                >
                  <option value="none">none</option>
                  <option value="optional">optional</option>
                  <option value="required">required</option>
                </select>
              </label>
              <label className="wizard__label wizard__label--half">
                <span>{t.wizard.importExportNeed}</span>
                <select
                  className="wizard__select"
                  onChange={(e) =>
                    update("importExportNeed", e.target.value as ImportExportNeed)
                  }
                  value={form.importExportNeed}
                >
                  <option value="none">none</option>
                  <option value="json">json</option>
                  <option value="file">file</option>
                  <option value="both">both</option>
                </select>
              </label>
            </div>

            <div className="wizard__row">
              <label className="wizard__label wizard__label--half">
                <span>{t.wizard.shareNeed}</span>
                <select
                  className="wizard__select"
                  onChange={(e) => update("shareNeed", e.target.value as ShareNeed)}
                  value={form.shareNeed}
                >
                  <option value="none">none</option>
                  <option value="optional">optional</option>
                  <option value="required">required</option>
                </select>
              </label>
              <label className="wizard__label wizard__label--half">
                <span>{t.wizard.pwaNeed}</span>
                <select
                  className="wizard__select"
                  onChange={(e) => update("pwaNeed", e.target.value as PwaNeed)}
                  value={form.pwaNeed}
                >
                  <option value="none">none</option>
                  <option value="basic">basic</option>
                  <option value="recommended">recommended</option>
                </select>
              </label>
            </div>
          </fieldset>
        </div>

        {/* Outputs */}
        <div className="wizard__outputs" data-testid="wizard-outputs">
          <h3 className="wizard__outputs-title">
            <Sparkles size={18} aria-hidden="true" />
            {language === "zh" ? "实时生成物" : "Live Outputs"}
          </h3>

          {!hasContent ? (
            <div className="wizard__empty">
              <p>
                {language === "zh"
                  ? "填写左侧表单，生成物将实时出现。"
                  : "Fill in the form on the left. Outputs appear in real time."}
              </p>
            </div>
          ) : (
            <div className="wizard__output-list">
              {OUTPUTS.map((def) => {
                const content = outputs[def.key];
                const isExpanded = expandedOutput === def.key;
                const preview = content.split("\n").slice(0, 4).join("\n");
                return (
                  <div
                    className={`wizard__output-card ${isExpanded ? "is-expanded" : ""}`}
                    data-testid={`wizard-output-${def.key}`}
                    key={def.key}
                  >
                    <button
                      className="wizard__output-header"
                      onClick={() => setExpandedOutput(isExpanded ? null : def.key)}
                      type="button"
                    >
                      {def.icon}
                      <span>{language === "zh" ? def.titleZh : def.titleEn}</span>
                    </button>
                    <pre className="wizard__output-preview">
                      {isExpanded ? content : preview}
                    </pre>
                    <div className="wizard__output-actions">
                      <CopyButton
                        copiedLabel={language === "zh" ? "已复制" : "Copied"}
                        label={language === "zh" ? "复制" : "Copy"}
                        onCopied={() =>
                          showToast(
                            language === "zh"
                              ? `已复制 ${def.titleZh}`
                              : `Copied ${def.titleEn}`,
                          )
                        }
                        onError={() =>
                          showToast(
                            language === "zh" ? "复制失败" : "Copy failed",
                            "danger",
                          )
                        }
                        text={content}
                      />
                      <DownloadButton
                        content={content}
                        fileName={def.fileName}
                        label={language === "zh" ? "下载" : "Download"}
                        mimeType={def.mimeType}
                        onDownloaded={() =>
                          showToast(
                            language === "zh"
                              ? `已下载 ${def.fileName}`
                              : `Downloaded ${def.fileName}`,
                          )
                        }
                      />
                    </div>
                  </div>
                );
              })}
            </div>
          )}
        </div>
      </div>
    </section>
  );
}
