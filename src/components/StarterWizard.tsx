function generateCreateCommand(state: WizardFormState): string {
  const parts = ["npm run create:project --"];
  if (state.repoSlug) parts.push(`--name ${state.repoSlug}`);
  if (state.projectName) parts.push(`--title "${state.projectName}"`);
  parts.push(`--level ${state.level}`);
  if (state.description) parts.push(`--description "${state.description}"`);
  parts.push("--dry-run");
  return parts.join(" ");
}

